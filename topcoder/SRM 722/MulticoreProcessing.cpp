struct MulticoreProcessing
{
    long long jobLength; 
    int corePenalty; 
    vector<int> speed; 
    vector<int> cores; 
    ll solve(ll len, int p, int speed, int cores) {
        ll val = p ? (ll)pow((long double)len/(1.0*speed*p), 0.5): cores;
        if(val > cores) val = cores;
        if(val < 1) val = 1;
        ll ret = 1LL*(val - 1)*p + (len - 1)/(speed*val) + 1;
        if(val < cores) ret = min(ret, 1LL*(val)*p + (len - 1)/(speed*(val+1)) + 1);
        return ret;
    }
    long long fastestTime(long long jobLength, int corePenalty, vector<int> speed, vector<int> cores)
    {
        this->jobLength = jobLength; 
        this->corePenalty = corePenalty; 
        this->speed = speed; 
        this->cores = cores; 
        ll ans = INF;
        FOR0(i, SZ(speed)) {
            ans = min(solve(jobLength, corePenalty, speed[i], cores[i]) , ans);
        }
        return ans;
    }
};

